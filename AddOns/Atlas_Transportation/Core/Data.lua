local function ISQ(quest_id)
	local str = ""
	local isCompleted = false
	if (not quest_id) then return str end
	
	isCompleted = C_QuestLog.IsQuestFlaggedCompleted(quest_id)
	
	if (isCompleted) then
		str = "" 
	else
		str = GREY..ALC["L-Parenthesis"]..UNAVAILABLE..ALC["R-Parenthesis"]
	end
	
	return str
end


